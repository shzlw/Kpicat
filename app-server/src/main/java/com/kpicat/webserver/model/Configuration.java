package com.kpicat.webserver.model;

public class Configuration {

    private String sidebarBgColor;

    private String sidebarFontColor;

    private String toolbarBgColor;

    private String toolbarFontColor;

    private String splashBgColor;

    private String splashFontColor;

    private String splashImage;

    private String splashText;

    public Configuration() {}

    public String getSidebarBgColor() {
        return sidebarBgColor;
    }

    public void setSidebarBgColor(String sidebarBgColor) {
        this.sidebarBgColor = sidebarBgColor;
    }

    public String getSidebarFontColor() {
        return sidebarFontColor;
    }

    public void setSidebarFontColor(String sidebarFontColor) {
        this.sidebarFontColor = sidebarFontColor;
    }

    public String getToolbarBgColor() {
        return toolbarBgColor;
    }

    public void setToolbarBgColor(String toolbarBgColor) {
        this.toolbarBgColor = toolbarBgColor;
    }

    public String getToolbarFontColor() {
        return toolbarFontColor;
    }

    public void setToolbarFontColor(String toolbarFontColor) {
        this.toolbarFontColor = toolbarFontColor;
    }

    public String getSplashBgColor() {
        return splashBgColor;
    }

    public void setSplashBgColor(String splashBgColor) {
        this.splashBgColor = splashBgColor;
    }

    public String getSplashFontColor() {
        return splashFontColor;
    }

    public void setSplashFontColor(String splashFontColor) {
        this.splashFontColor = splashFontColor;
    }

    public String getSplashImage() {
        return splashImage;
    }

    public void setSplashImage(String splashImage) {
        this.splashImage = splashImage;
    }

    public String getSplashText() {
        return splashText;
    }

    public void setSplashText(String splashText) {
        this.splashText = splashText;
    }
}
